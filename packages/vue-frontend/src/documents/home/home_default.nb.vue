# Tingtun TERMER

Forbedre leseforsåelsen og leseflyten med direkte oppslag i relevante ordlister. Løsningen er laget for å være tilgjengelig for alle brukere på PC, nettbrett eller mobil og uavhengig av brukerens funksjonsevne.

Kildene for oppslag dekker generelle ordbøker og spesielle fagbegreper fra områder som beredskap, helse og jus på norsk. Vi kan også inkludere terminologier for mer enn 500 språkpar for oversettelser.

## Fagområder klare for test
- Ordforklaringer fra beredskap og redning: [insitu.termer.no](https://insitu.termer.no/search)
- Ordforklaringer fra juss og oppslag fra norske lover og EU forordninger: [jus.termer.no](https://jus.termer.no/search)
- Ordforklaringer fra helse og medisin: [helse.termer.no](https://helse.termer.no/search)
- Ordforklaringer fra universell utforming (accessibility): [wcag.termer.no](https://wcag.termer.no/search)

Vi jobber kontinuerlig med å forbedre tjenesten. Bruk gjerne tilbakemeldingsknappen om du finner feil eller har forslag.

Innen beredskap og krisehåndtering på nettsidene til [Hovedredningssentralen](https://www.hovedredningssentralen.no/), [Næringslivets sikkerhetsorganisasjon](https://nso.no/) og til [Kollegiet for brannfaglig terminologi (KBT)](http://kbt.no).
