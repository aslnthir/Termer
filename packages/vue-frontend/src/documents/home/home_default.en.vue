# Tingtun TERMER

Keep the reading flow and Increase literacy with direct access to relevant dictionaries. The application is designed to be accessible for all users regardless of disabilities and Works for PC, tablet and Smartphone.

Dictionaries can cover specific definitions from domains like health, energy, law, or general language sources to provide meanings or translations for concepts. Terminologies for more than 500 language combinations can be made available.

## Available test domains
- Terminology for EU institutions and search for directives: [iate.termer.no](https://iate.termer.no/search)
- Web accessibility terminology: [wcag.termer.no](https://wcag.termer.no/)
- Selection of English glossaries: [english.termer.no](https://english.termer.no/search)
