## Install Termer for Edge

1. Press the footerbar “Add Termer to Home screen”. Alternatively you can press
   the Edge-menu, and select “Add to Home screen”
   <img src="@/assets/images/App-Installation-Edge-en-1.jpg"/>

2. Press “Add”
   <img src="@/assets/images/App-Installation-Edge-en-2.jpg"/>

3. Press “Add”, or press the app icon to place it on your home screen.
   <img src="@/assets/images/App-Installation-Edge-en-3.jpg"/>

4. The app should now be added to your home screen. Please note that it can be
   located on another desktop and you may want to move it to your home screen.
   <img src="@/assets/images/App-Installation-Edge-en-4.jpg"/>
