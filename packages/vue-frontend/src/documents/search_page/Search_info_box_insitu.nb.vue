**Velkommen til INSITU Termer**

Dette er en søketjeneste for begreper, forkortelser og symboler innen beredskap og redning. Tjenesten samler mer enn 30 norske ordlister i et søk. Noen ord har forklaringer i flere ordlister. Treff fra <a href="https://box.tingtun.no/f/c4ef107d491f4d3e9b23/?dl=1" title="last ned Redningshåndboken" download>Redningshåndbokens</a> ordlister blir da vist først. Målet er å bidra til en felles situasjonsforståelse for de som samhandler innen beredskap og redning.

Velg språk og kilder for søk med knappen for innstillinger
<config-button style="font-size:.6em; vertical-align:bottom" />.

Tjenesten er utviklet av Tingtun AS for [INSITU-prosjektet](http://insitu.uia.no), med støtte fra [NFR/SAMRISK](https://www.forskningsradet.no/om-forskningsradet/programmer/samrisk/). Du kan bidra til forbedringer av tjenesten med forslag til nye ordlister eller andre kommentarer. Bruk gjerne knappen for "Tilbakemelding".
