## Installere Termer i nettleseren
Installere Termer som bookmarklet i nettleseren.

- få frem bokmerkerad i nettlesern
- Åpne nettsiden [Termer.no/bookmarklet](https://Termer.no/bookmarklet)
- installere bookmarklet

For å teste at Termer virker i nettleseren kan du prøve følgende
- Gå til Wikipediaside om [tekst](https://no.wikipedia.org/wiki/Tekst)
- Klikk på TERMER Demo i bokmerkeraden
- Dobbelklikk på “kjennetegn” og trykk enter

