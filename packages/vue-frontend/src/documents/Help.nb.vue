## Instillinger
Innstillinger for språk og valg av ordbøker
- Åpne nettsiden [Termer.no](https://Termer.no)
- Søk på “start“
- klikk på tannhjulet (knappen for innstillinger)
- Klikk på Målspråk
- Legg til arabisk som målspråk
- Endre rekkefølge på ordbøker slik at Lexin kommer først
- Fjern Bokmålsordboka
- Legg til Store Medisinske Leksikon
- Søk på "feber"

