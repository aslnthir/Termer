## Søk fra PDF-dokument
- last ned og lagre PDF dokumentet om tekst fra Wikipedia, ["her"](https://no.wikipedia.org/w/index.php?title=Spesial:DownloadAsPdf&page=Tekst&action=show-download-screen).
