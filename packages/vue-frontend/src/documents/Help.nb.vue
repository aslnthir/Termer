## Søk fra termer.no
Følgende eksempel viser varianter for søk.
- Åpne nettsiden [Termer.no](https://Termer.no)
- Søk på ordet "start"
- Klikk på "forkortelse" som har understrek, fra trefflisten for "start"
- dobbelklikk på "talespråket" og trykk enter
- Markér "skrift" og trykk enter

