## Send tilbakemelding
Send oss gjerne tilbakemeldinger om feil eller forslag til forbedringer av verktøyet Termer. Vi vil og gjerne ha tilbakemeldinger på oversettelser eller ordforklaringer som vi kan formidle til dem som eier dem.
- Åpne nettsiden [Termer.no](https://Termer.no)
- Klikk på knappen "Tilbakemelding"
- Skriv inn melding og trykk på send

Om du vil at vi skal ta kontakt med deg så kan du sette inn epost adressen din i tilbakemeldingen. Vi kommer til å legge ut en nettside med oppsummeringer av tilbakemeldinger sammen med beskrivelser av feil som er rettet og andre endringer.

