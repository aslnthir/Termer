# Tingtun TERMER

Slå opp ordforklaringer og oversettelser fra et utvalg av ordbøker. Tjenesten
er utviklet for å være universelt utformet, bevare personvern og fungere for alle uavhengig av
funksjonsnedsettelser, valg av nettleser eller utstyr nettbrett, smarttelefon eller PC. Tingtun TERMER
er utviklet og levert av <a href="https://tingtun.no/" target="_blank">Tingtun AS</a>.

TERMER blir blant annet brukt på nettsidene til <a href="https://hovedredningssentralen.no" target="_blank">Hovedredningssentralen</a> som
forvalter begreper for effektiv krisehåndtering på tvers av organisasjoner i Norge.

TERMER har også blitt brukt i flere forskningsprosjekter. Deriblant INSITU prosjektet for koordinering og bruk av begreper innen redning og beredskap, med støtte fra Norges Forskningsråd, Mobile Age-prosjektet, med støtte fra EU-kommisjonen, og prosjekter med fokus på universell utforming med støtte fra UNIKT og fra Regionalt næringsfond Birkenes, Iveland og Lillesand.
