# Tingtun Termer

Translate selected texts covering any combination of over 30 language and look up the meaning of concepts from selected dictionaries.

Dictionaries can cover specific areas like health, energy, law, or general language words or translations.
Terminologies for more than 500 language combinations used in the European Parliament, and several general dictionaries can be used.

The Tingtun Termer services are developed and delivered by <a href="https://tingtun.no/" target="_blank">Tingtun AS</a>.
