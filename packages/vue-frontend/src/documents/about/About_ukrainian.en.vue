# Tingtun Termer

Translate selected texts covering any combination of over 30 languages including Ukrainian, and look up the meaning of concepts from selected dictionaries.

The service is designed to be accessible for all, regardless of disabilities or browser preferences, to work across PCs, tablets and Smartphones, and to preserve user privacy.

Termer development has been supported by Horizon 2020 in the Mobile Age project. The Tingtun Termer services are delivered by <a href="https://tingtun.no/" target="_blank">Tingtun AS</a>.
