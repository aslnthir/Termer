# Tingtun Termer

Oversett tekster mellom over 30 ulike språk inkludert Ukrainsk og slå opp ordforklaringer fra et utvalg av ordbøker. Tjenesten
er utviklet for å være universelt utformet, bevare personvern og fungere for alle uavhengig av
funksjonsnedsettelser, valg av nettleser eller om du bruker nettbrett, smarttelefon eller PC. Tingtun Termer
er utviklet og levert av <a href="https://tingtun.no/" target="_blank">Tingtun AS</a>.

Termer har blitt utviklet med støtte fra Horizon 2020 i  Mobile Age-prosjektet,
og tidligere prosjekter med fokus på universell utforming med støtte fra UNIKT og
fra Regionalt næringsfond Birkenes, Iveland og Lillesand.
