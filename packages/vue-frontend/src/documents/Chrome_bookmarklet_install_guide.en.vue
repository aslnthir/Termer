## Chrome guide

1. Set focus to bookmarklet link “TERMER demo”
1. Bring up Link context menu (Shift + F10)
1. Then select the option to copy link location
1. Open Bookmark manager (Ctrl + Shift + O)
1. Top right hand menu (Organize) -> Add new bookmark
1. Type “Termer” in the name field (name). Paste bookmarklet link address in the address field (URL)
