# Personvernerklæring

Denne personvernerklæringen beskriver hvordan tjenesten Tingtun Termer samler og behandler persondata.

## Databehandling
Innsamlet data blir brukt til å lagre brukerinnstillinger for å konfigurere
Tingtun Termer, for vedlikehold og til videre utvikling av tjenestene.
Dataene blir ikke delt med noen tredjepart, unntatt om dette skulle
bli lovpålagt.
Anonymiserte data fra oppslag vil bli lagret i 12 måneder og eldre data kan
bli permanent lagret i aggregert og anonymisert form.

## Datainnsamling
Følgende opplysninger blir lagret. For registrerte brukere
som gir sitt samtykke kan vi lagre epost, passord, innhold i personlige
ordbøker, og utvalg av ordbøker. Vi lagrer ikke opplysninger som kan bli brukt
til å knytte en bruker til et bestemt oppslagsord.

Nettsidene knyttet til tjenestene inkludert PDF leseren og søkesiden vil lagre
IP-addresse fra klikkstrøm i en web-log som blir slettet hver tredje måned.

Lovhjemmel for databehandlingen er gitt av artikkel 6 i GDPR.

## E-post og bruk av kontaktskjema

E-post med informasjon om Tingtun Termer-tjenester kan bli sendt til abonnenter
av tjenesten. E-postadresser registrert for abonnement vil ikke bli brukt til
noe annet formål eller bli delt med andre.

Kontaktskjema kan bli brukt til spørsmål eller andre tilbakemeldinger. Husk å sende med
din e-postadresse om du vil at vi skal ta kontakt med deg. Henvendelser vil bli
besvart individuelt. Anonymiserte henvendelser kan bli brukt i en offentlig
liste over spørsmål og svar.

