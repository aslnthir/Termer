## Installer Termer for Edge

1. Trykk på «Add Termer to Home screen». Alternativt, åpne Edge-menyen og velg
   «Add to Home screen»
   <img src="@/assets/images/App-Installation-Edge-en-1.jpg"/>

2. Velg «Add»
   <img src="@/assets/images/App-Installation-Edge-en-2.jpg"/>

3. Velg «Add» eller hold nede fingeren på app-ikonet for å plassere det på
   hjemmeskjermen.
   <img src="@/assets/images/App-Installation-Edge-en-3.jpg"/>

4. Appen skal nå være klar til bruk fra din hjemmeskjerm.
   <img src="@/assets/images/App-Installation-Edge-en-4.jpg"/>
