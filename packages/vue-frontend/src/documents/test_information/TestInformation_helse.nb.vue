# Spørsmål og svar om TERMER

## Hvorfor blir TERMER-knappen ikke vist?
Noen nettsider har en blokkering som gjør at TERMER-knappen ikke blir vist. Et eksempel er [Helsenorge.no](https://www.helsenorge.no/). Administror av nettsiden kan åpne for TERMER.

## Hvorfor blir begreper ikke understreket?
Begreper som forekommer i en aktivert ordbok kan bli understreket. Begreper i lenker og overskrifter blir ikke markert for bedre lesbarhet. Gjentatte begreper i samme avsnitt blir heller ikke markert.
For store ordbøker blir begreper ikke markert. For eksempel så blir ord fra ICNP markert, men ikke fra Wikipedia.

Termer kan bare markere begreper som overenstemmer eksakt med skrivemåten i de ordbøker som er aktivert. Prøv gjerne å søke på grunnformen av et ord om du ikke får treff. For eksempel gir et søk på "feber" treff fra ICNP mens "feberen" ikke gjør det.

## Hvordan kan jeg bruke TERMER på PDF?
Det kan man gjøre på to måter.
1. Når en PDF fil er lenket fra en nettside kan man åpne den med automatisk understrekning for oppslag. Et eksempel for dette er Redningshåndboken som er lenket fra en nettside på [Hovedredningssentralen](https://www.hovedredningssentralen.no/redningshandboken-er-endelig-her/).
2. Med PDF-knappen kan man lese PDF dokumenter som er lagret lokalt på en PC. Prøv gjerne fra [helse.termer.no/search](https://helse.termer.no/search).

