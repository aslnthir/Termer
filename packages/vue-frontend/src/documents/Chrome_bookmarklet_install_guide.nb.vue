## Guide for Chrome

1. Flytt fokus til lenken «TERMER demo»
1. Åpne lenke-menyen (Shift + F10)
1. Kopiere lenkeadressen
1. Lag et bokmerke av denne siden (Ctrl + d)
1. Lagre bokmerket i bokmerkeraden (bookmarks toolbar)
1. Håndtere bokmerker (Ctrl+Shift+o)
1. Navigere til det nye bokmerket
1. Åpne det nye bokmerket for å endre det
1. Gi det nye bokmerket navnet "Termer"
1. Gå til feltet for URL og erstatt URLen med den kopierte lenkeadressen (Ctrl+a , Ctrl+v)
