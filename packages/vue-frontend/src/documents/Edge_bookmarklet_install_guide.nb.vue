## Guide for Edge

1. Åpne favoritter-menyen (Ctrl - I)
   - Velg innstillinger (tannhjul)
   - Sett «Vis favoritter-linjen» («Show the favorites bar») til «PÅ»
1.    Flytt fokus til Termer-lenken på siden
1.    Åpne lenkemenyen (Shift + F10)
1.    Kopier lenken (Ctrl - C)
1.    Legg til siden i favoritter (Ctrl - D)
1.    Skriv inn «Termer» i navnefeltet
1.    Under «Lagre i» («Save in»), velg «Favoritter-linjen» («Favorites Bar»)
1.    Trykk på «Legg til» («Add»)
1.    Åpne favoritter-menyen (Ctrl - I)
1.    Flytt fokus til bokmerket som du nettopp la til
1.    Åpne lenkemenyen (Shift + F10)
1.    Velg «Endre URL» («Edit URL»)
1.    Lim inn lenken i URL-feltet (Ctrl - V)
1.    Trykk enter
1.    Ferdig
