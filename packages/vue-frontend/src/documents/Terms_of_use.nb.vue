# Bruksbetingelser for Tingtun Termer

Vennligst les disse bruksbetingelsene før du bruker Tingtun Termer.
Tingtun forbeholder seg retten til å når som helst tilbakekalle eller
endre tjenestene og bruksbetingelsene. Tilgang til og bruk av tjenestene
forutsetter nedenstående betingelser.

Se [Personvernerklæring](https://termer.no/privacy) for nærmere om dette tema.

Innholdet presentert på søkesiden eller gjennom Tingtun Termer (tjenestene) er
i utgangspunktet sammenstilt for bruk i forbindelse med [PRUK prosjektet](https://www.uia.no/senter-og-nettverk/uia-puls-pedagogisk-utviklings-og-laeringssenter/program-for-utdanningskvalitet-pruk) og [INSITU-prosjektet](https://insitu.uia.no/om/). Annen bruk kan skje etter avtale med Tingtun AS (Tingtun).

Informasjonen presentert gjennom tjenestene er gitt til deg «som den er», kun
til bruk som din informasjon. Tingtun og tilknyttede partnerorganisasjoner garanterer på ingen måte for nøyaktigheten eller
fullstendigheten av informasjonen, dette inkluderer enhver underforstått
garanti for kvalitet, salgbarhet, forutsatte egenskaper eller rettsstrid. Som
bruker må du ta det hele og fulle ansvar for eventuelle skader som måtte oppstå
som en følge av bruk av tjenestene, bruk av data fra tjenestene, eller
distribusjon av data fra tjenestene.

I tillegg påtar Tingtun seg intet ansvar for andre Internettsider du måtte gå
til via disse Internettsidene. Linker gitt av Tingtun til slike Internettsider
er kun inkludert for å gi en bedre brukeropplevelse, og betyr ikke at Tingtun
støtter eller påtar seg noe ansvar for disse sidene eller innholdet i dem.

