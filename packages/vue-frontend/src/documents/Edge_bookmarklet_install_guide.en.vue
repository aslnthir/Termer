## Edge guide

1. Show the favorites-menu (Ctrl-I)
   - Select settings (cog-wheel)
   - Set "Show the favorites bar" to "ON"
1.    Set focus to bookmarklet link "TERMER demo"
1.    Bring up Link context menu (Shift + F10)
1.    Copy the link (the bookmarklet code)
1.    Add page to favorites (Ctrl - D)
1.    Select menu option "Store in favorites field"
1.    Rename the bookmarklet and Press the "Add" button
1.    Bring up the bookmarks management menu (Ctrl - I)
1.    Select the newly created bookmarklet in the favourites field
1.    Bring up the Link context menu (Shift + F10)
1.    Select the menu option "Edit URL"
1.    Paste the copied link (code) into the URL field
1.    Press Enter
1.    Done.
