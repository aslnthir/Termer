# Bruksbetingelser for Tingtun Termer

Vennligst les disse bruksbetingelsene før du bruker Tingtun Termer, inkludert søkeside, utvidelse til nettleseren (bookmarklet), eller appen (PWA).
Tingtun forbeholder seg retten til å når som helst tilbakekalle eller
endre tjenestene og bruksbetingelsene. Tilgang til og bruk av tjenestene forutsetter nedenstående betingelser.

Se [Personvernerklæring](https://termer.no/privacy) for nærmere om dette tema.

Innholdet presentert på søkesiden eller gjennom Tingtun Termer (tjenestene) er
i utgangspunktet sammenstilt for bruk i forbindelse med oversettelser og språkælæring for flyktninger fra Ukraina og for dem som bistår dem. Denne bruksretten er gitt, kostnadsfritt og minst frem til 2022-12-31. Annen bruk kan skje etter avtale med Tingtun AS.

Informasjonen presentert gjennom tjenestene er gitt «som den er», kun til bruk for informasjon. Tingtun og tilknyttede partnerorganisasjoner garanterer på ingen måte for nøyaktigheten eller fullstendigheten av informasjonen, dette inkluderer enhver underforstått garanti for kvalitet, salgbarhet, forutsatte egenskaper eller rettsstrid. Som
bruker må du ta det hele og fulle ansvar for eventuelle skader som måtte oppstå som en følge av bruk av tjenestene, bruk av data fra tjenestene, eller distribusjon av data fra tjenestene.

I tillegg påtar seg Tingtun intet ansvar for andre Internettsider du måtte gå
til via våre tjenester. Linker gitt av Tingtun til slike Internettsider
er kun inkludert for å gi en bedre brukeropplevelse, og betyr ikke at Tingtun
støtter eller påtar seg noe ansvar for disse sidene eller innholdet på dem.

