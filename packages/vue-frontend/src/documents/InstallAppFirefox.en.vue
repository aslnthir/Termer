## Install Termer for Firefox

1. Tap the house symbol next to the URL on top of the browser.
   <img src="@/assets/images/App-Installation-Firefox-en-1.jpg"/>

2. Press the button “ADD TO HOME SCREEN”.
   <img src="@/assets/images/App-Installation-Firefox-en-2.jpg"/>

3. Hold down the app icon to place on your home screen, or press "Add"
   <img src="@/assets/images/App-Installation-Firefox-en-3.jpg"/>

4. The app should now be added to you home screen. Please note that it can be
   located on another desktop and you may want to move it to your home
   screen.
   <img src="@/assets/images/App-Installation-Firefox-en-4.jpg"/>
