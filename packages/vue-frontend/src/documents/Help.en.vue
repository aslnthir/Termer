## Search from PDF document
- Download a PDF document from Wikipedia, ["example document about text"](https://en.wikipedia.org/w/index.php?title=Special:DownloadAsPdf&page=Text_%28literary_theory%29&action=show-download-screen).
- Open the page [English.Termer.no](https://english.termer.no)
- Press the PDF button
- Open the PDF document you downloaded from Wikipedia
- Select “street sign” and press enter
- Add Wikipedia as a source
- Click on the Termer button
- Search for “work of art” using the search field

