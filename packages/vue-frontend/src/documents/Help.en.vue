## Install and start Termer in the browser
- Show the bookmarks bar in the browser (Ctrl-Shift-B)
- Open the webpage [Enligsh.Termer.no/install](https://English.Termer.no/install)
- Install Termer as described on the page
- Go to a Wikipedia page about text [tekst](https://en.wikipedia.org/wiki/Text_(literary_theory))
- Click TERMER in the bookmarks bar of the browser
- Click on the underlined “encyclopedia”

