## Search using Termer
Ways to navigate the search.
- Open the search page [English.Termer.no](https://english.Termer.no/search)
- Search for "start"
- Click the underlined "fibre", in the search result
- You can also search for selected text that is not underlined.
Double click "indigestible" to select the term, and press enter
- press the back arrow in the browser to return to the result for "fibre"
- Select "cell wall" as a portion of text to search, and press enter

