## Install Termer for Chrome

1. Press the menu (the three dots), and select “Add to Home screen”. Alternatively
   you can press the footerbar “Add Termer to Home screen”.
   <img src="@/assets/images/App-Installation-Chrome-en-1.jpg"/>

2. Select “Add to Home screen”
   <img src="@/assets/images/App-Installation-Chrome-en-2.jpg"/>

3. Press “Add”
   <img src="@/assets/images/App-Installation-Chrome-en-3.jpg"/>

4. The app should now be added to your home screen. Please note that it can be
   located on another desktop and you may want to move it to your home screen.
   <img src="@/assets/images/App-Installation-Chrome-en-4.jpg"/>
